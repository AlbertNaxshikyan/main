package chapter3;

public class Area {
    public static void main(String[] args) {
        double pi, r, a;
        r = 10.8; // Радиус окружности
        pi = 3.1416; // Приблизительное значение числа пи
        a = pi * r * r; // Вычислить площадь круга.
        System.out.println(" Площадь круга равна: " + a);
    }
}
